package day07_Operators;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int num1= 9;
        int num2= 3;
        int division = num1/num2;// result of 10/3
        int remainder = num1 % num2;//result of 10/3 with a remainder of 1
        System.out.println(num1+" divided by "+num2+" is equal to "+division+" with a remainder of "+remainder);

        


        // 10 divide by 3 equals 3 with a remainder of 1
    }
}
